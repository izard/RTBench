#include <param.h>
#include <segments.h>
#include <lwrte_config.h>

isr_t interrupt_handlers[256];

void register_interrupt_handler(u8int n, isr_t handler)
{
    interrupt_handlers[n] = handler;
}

// This gets called from our ASM interrupt handler stub.
void isr_handler(registers_t regs)
{
    if (interrupt_handlers[regs.int_no] != 0)
    {
        isr_t handler = interrupt_handlers[regs.int_no];
        handler(regs);
    }
    else
        kprintf("Recieved unhandled ISR interrupt: %d\n ", regs.int_no);
}

// This gets called from our ASM interrupt handler stub.
void irq_handler(registers_t regs)
{
    // IRQ7 is a spurious interrupt
    if (regs.int_no == IRQ7)
    {
        //read from In-Service regs
        outb(0x20, 0x0B); unsigned char irr = inb(0x20);
        //If bit 7 is not set, return without sending EOI
        if(!(irr & 0x80)) return;
    }

    // Send an EOI (end of interrupt) signal to the PICs.
    // If this interrupt involved the slave.
    if (regs.int_no >= 40)
    {
        // Send reset signal to slave.
        outb(0xA0, 0x20);
    }
    // Send reset signal to master. (As well as slave, if necessary).
    outb(0x20, 0x20);

    if (interrupt_handlers[regs.int_no] != 0)
    {
        isr_t handler = interrupt_handlers[regs.int_no];
        handler(regs);
    }
    else
        kprintf("Recieved unhandled IRQ interrupt: %d\n ", regs.int_no);

}
